#ex02 - media

num1 = float(input('Informe o primeiro valor: '))
num2 = float(input('Informe o segundo valor: '))
num3 = float(input('Informe o terceiro valor: '))


resultado = ((num1 + num2 + num3)/3)


print('Media igual a {0:.2f}'.format(resultado))

