


-- TABELA COMPRIMIDA

CREATE TABLE TB_COMPRESSAO
(
ID INT IDENTITY PRIMARY KEY,
VALOR INT
)


DECLARE @valor INT =1
WHILE @valor <= 100000
BEGIN
INSERT INTO TB_COMPRESSAO VALUES (@valor)
SET @valor += 1
END
GO


EXEC SP_SPACEUSED TB_COMPRESSAO


ALTER TABLE TB_COMPRESSAO
REBUILD WITH (DATA_COMPRESSION = ROW);
GO
